return {
  {
    "phaazon/mind.nvim",
    dependencies = {

      "phaazon/mind.nvim",
    },
    config = function()
      vim.keymap.set("n", "<leader>m", function()
        require("mind").open_project()
      end)
      vim.keymap.set("n", "<leader>M", function()
        require("mind").open_main()
      end)
      require("mind").setup()
    end,
  },
}
